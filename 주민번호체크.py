import re

# 주민등록번호를 체크하는 정규식
pattern = r'^\d{6}-?[1-4]\d{6}$'

# 정규식에 맞는 샘플 코드
sample_codes = [
    "951212-1234567",
    "9512121234567",
    "040101-1234567",
    "0401011234567",
    "800101-1234567",
    "8001011234567",
    "120202-2345678",
    "1202022345678",
    "991231-9876543",
    "9912319876543",
    "951212-0234567",  # "-" 누락
    "030101-9234567",  # 3번째 자리 숫자 오류
    "071212-5123457",  # 4번째 자리 숫자 오류
]

for code in sample_codes:
    if re.match(pattern, code):
        print(f"{code}은 유효한 주민등록번호입니다.")
    else:
        print(f"{code}은 유효하지 않은 주민등록번호입니다.")
